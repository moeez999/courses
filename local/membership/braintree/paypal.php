function get_paypal_subscription_details($subscriptionId, $accessToken)
{
    $url = "https://api-m.paypal.com/v1/billing/subscriptions/" . urlencode($subscriptionId);

    $ch = curl_init();
    curl_setopt($ch, CURLOPT_URL, $url);
    curl_setopt($ch, CURLOPT_HTTPHEADER, [
        "Authorization: Bearer $accessToken",
        "Content-Type: application/json"
    ]);
    curl_setopt($ch, CURLOPT_RETURNTRANSFER, true);
    curl_setopt($ch, CURLOPT_SSL_VERIFYPEER, false);  // For local/dev only
    curl_setopt($ch, CURLOPT_SSL_VERIFYHOST, false);  // For local/dev only

    // ‚ùå DO NOT set CURLOPT_POST for a GET request
    // curl_setopt($ch, CURLOPT_POST, true);  <-- REMOVE THIS

    $response = curl_exec($ch);

    if (curl_errno($ch)) {
        echo 'Curl error: ' . curl_error($ch);
        curl_close($ch);
        return null;
    }

    $httpCode = curl_getinfo($ch, CURLINFO_HTTP_CODE);
    curl_close($ch);

    if ($httpCode >= 200 && $httpCode < 300) {
        return json_decode($response, true); // Success
    } else {
        echo "Failed. HTTP Code: $httpCode. Response: $response";
        return null;
    }
}


